export const getUserPrompt = (config) => `
<작성 요청>
아래 정보에 따라 블로그 원고를 작성하세요.
모든 조건을 충족하면서 자연스럽고 경험 중심의 글을 만들어주세요.
메인 키워드는 의미에 맞게 변환해서는 안 되며, 텍스트 그대로 사용해야 합니다.

${NUMBER_WARNING}

<기본정보>
- **제목 작성 규칙**:
  * 메인 키워드 "${config.mainKeyword}" + 서브키워드 중 하나 + "${config.marketName}" 포함
  블로그 제목답게 자연스럽고 친근하게 작성
  예시:
   1. 김해 소고기 맛집 가성비 좋은 고기집 담가화로구이
   2. 창업 브랜드 고기 맛집 담가화로구이
- 메인 키워드: ${config.mainKeyword}
- ${KEYWORD_RULE}
- 글자 수: ${config.minLength}자 이상 ${config.maxLength}자 이하
- 단락 수: ${config.imageCount}개
- 글 성격: ${config.contentType} 스타일

<핵심 내용>
${config.transformedContent}

**중요 지침**:
1. 제공된 성공사례 중 랜덤하게 3~4개를 선택하여 사용
- 선택한 사례는 자연스럽게 본문에 녹여서 작성
- 점포명은 그대로 유지
   
2. **내용 구조를 창의적으로 재배치**
- 특징, 성공사례, 세미나 정보, 시스템 등을 자유롭게 순서 변경
- 예: 도입부 → 성공사례 → 특징 → 세미나 → 마무리
- 또는: 도입부 → 특징 → 세미나 → 성공사례 → 마무리
- 또는: 도입부 → 세미나 → 성공사례 → 특징 → 마무리
- 같은 정보라도 전개 순서를 매번 다르게 구성

3. 핵심 내용도 모두 다 들어가지 않아도 되니 적절히 선택하여 작성

<마무리 정보>
${config.additionalRequirements}
`;
